"""
for循环（遍历循环）

for 变量 in  被遍历的数据:
    循环执行的代码



可以被遍历的基本数据类型：列表，字符串，元祖，字典，集合


内置函数len()：获取数据的长度


"""

li = ["aa", "bb", "cc", "dd", "ee", "ff"]

print("列表的长度：",len(li))


for i in li:
    print("11111循环体中的代码11111111")
    print(i)
    print("222222循环体中的代码222222")
